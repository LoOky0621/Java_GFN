package uebung1;

public class StaticRechner {

    public static double add(double a, double b) {
        return a + b;
    }

    public static double sub(double a, double b) {
        return a - b;
    }

    public static double div(double a, double b) {
        return a / b;
    }

    public static double multi(double a, double b) {
        return a * b;
    }
}
